    private JPanel criarCabecalhoComBotaoFechar(String titulo) {
        // Cria um painel para o cabeçalho da aba
        JPanel cabecalho = new JPanel(new FlowLayout(FlowLayout.LEFT, 0, 0));
        cabecalho.setOpaque(false); // Deixa o painel transparente para combinar com o visual da aba

        // Adiciona um rótulo com o título da aba
        JLabel labelTitulo = new JLabel(titulo);
        cabecalho.add(labelTitulo);

        // Cria um botão de fechar "x"
        JButton botaoFechar = new JButton("x");
        botaoFechar.setMargin(new Insets(0, 0, 0, 0));
        botaoFechar.setPreferredSize(new Dimension(17, 17));

        // Adiciona uma ação ao botão para fechar a aba ao clicar
        botaoFechar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Encontra o índice da aba associada a este cabeçalho
                int index = PainelAbas.indexOfTabComponent(cabecalho);
                if (index != -1) {
                    PainelAbas.remove(index);
                }
            }
        });
        // Adiciona o botão de fechar ao cabeçalho
        cabecalho.add(botaoFechar);

        return cabecalho;
    }

