    private void BtnListagemProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnListagemProdutosMouseClicked
        // TODO add your handling code here:
        // Verifica se já existe uma aba para "ListagemClientes" antes de adicionar
        boolean existe = false;
        int tabCount = PainelAbas.getTabCount();

        for (int i = 0; i < tabCount; i++) {
            if (PainelAbas.getTitleAt(i).equals("Produtos")) {
                existe = true;
                PainelAbas.setSelectedIndex(i); // Seleciona a aba existente
                break;
            }
        }

        if (!existe) {
            // Cria um novo painel com o formulário de clientes
            ListagemDeProdutos listagemDeProdutos = new ListagemDeProdutos();
            PainelAbas.addTab("Produtos", listagemDeProdutos);
            int index = PainelAbas.indexOfComponent(listagemDeProdutos);
            PainelAbas.setTabComponentAt(index, criarCabecalhoComBotaoFechar("Produtos"));
            PainelAbas.setSelectedComponent(listagemDeProdutos);
        }

    }//GEN-LAST:event_BtnListagemProdutosMouseClicked

